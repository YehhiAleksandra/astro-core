use serde::Serialize;

pub const ZODIAC_SIGNS_RU: [&str; 12] = [
    "\u{041e}\u{0432}\u{0435}\u{043d}",
    "\u{0422}\u{0435}\u{043b}\u{0435}\u{0446}",
    "\u{0411}\u{043b}\u{0438}\u{0437}\u{043d}\u{0435}\u{0446}\u{044b}",
    "\u{0420}\u{0430}\u{043a}",
    "\u{041b}\u{0435}\u{0432}",
    "\u{0414}\u{0435}\u{0432}\u{0430}",
    "\u{0412}\u{0435}\u{0441}\u{044b}",
    "\u{0421}\u{043a}\u{043e}\u{0440}\u{043f}\u{0438}\u{043e}\u{043d}",
    "\u{0421}\u{0442}\u{0440}\u{0435}\u{043b}\u{0435}\u{0446}",
    "\u{041a}\u{043e}\u{0437}\u{0435}\u{0440}\u{043e}\u{0433}",
    "\u{0412}\u{043e}\u{0434}\u{043e}\u{043b}\u{0435}\u{0439}",
    "\u{0420}\u{044b}\u{0431}\u{044b}",
];

#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize)]
pub struct SunSign {
    pub index: usize,
    pub name_ru: &'static str,
}

pub fn sun_sign(day: u8, month: u8) -> SunSign {
    let index = match (month, day) {
        (3, 21..=31) | (4, 1..=19) => 0,
        (4, 20..=30) | (5, 1..=20) => 1,
        (5, 21..=31) | (6, 1..=20) => 2,
        (6, 21..=30) | (7, 1..=22) => 3,
        (7, 23..=31) | (8, 1..=22) => 4,
        (8, 23..=31) | (9, 1..=22) => 5,
        (9, 23..=30) | (10, 1..=22) => 6,
        (10, 23..=31) | (11, 1..=21) => 7,
        (11, 22..=30) | (12, 1..=21) => 8,
        (12, 22..=31) | (1, 1..=19) => 9,
        (1, 20..=31) | (2, 1..=18) => 10,
        _ => 11,
    };
    SunSign {
        index,
        name_ru: ZODIAC_SIGNS_RU[index],
    }
}

